public class String {
    public static void main(String[] args) {
		String a="hello";
		System.out.println("before reverse");
		System.out.println(a);

		 System.out.println("after reverse");
		 for (int i=a.length()-1;i>=0;i--)
			 
		 {
			 System.out.print(a.charAt(i));
		 }
	}

}
